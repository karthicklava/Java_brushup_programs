package basic;
public class PositiveOrNegative {

	
	public static void main(String[] args) {
		double dd= -0.56;
		
		if(dd<0.0){
			System.out.println("Number is negative");
		}
		else if(dd>0.0){
			System.out.println("Number is positive");
		}
		else{
			System.out.println("Number is zero");
		}
	}
}
